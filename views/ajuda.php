<h3>Ajuda</h3>
<p>Este sistema foi modularizado em:</p>
<ul>
  <li>Views por guia em <code>views/</code></li>
  <li>Estilos em <code>assets/css/app.css</code></li>
  <li>Scripts em <code>assets/js/app.js</code></li>
  <li>APIs em <code>api/</code> e clientes em <code>app/api/</code></li>
  <li>Modelos em <code>app/models/</code> e config em <code>app/config.php</code></li>
</ul>

